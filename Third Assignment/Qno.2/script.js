//For calculating area of triangle
const base = prompt("Enter the base of a triangle: ");
const height = prompt("Enter the height of a triangle: ");

// calculate the area
const area = (base * height) / 2;

console.log(`The area of the triangle is ${area}`);


//For calculating area of rectangle
var rectangleLength = parseInt(prompt("Enter length of rectangle:"));
var rectangleBreadth = parseInt(prompt("Enter breadth of rectangle:"));

var areaOfRectangle = rectangleLength * rectangleBreadth ;

console.log("Area of rectangle is: " + areaOfRectangle);
